

public class Salary {
    public static void main(String [] args) {
        int hourlyWage;

        hourlyWage = 20;

        System.out.print("Annual salary is: ");
        System.out.println(hourlyWage * 40 * 50);

        System.out.print("Monthly salary is: ");
        System.out.println((hourlyWage * 40 * 50) / 12);
    }
}
